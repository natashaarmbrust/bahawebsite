        <div class ="center">
            <h2>Static Events</h2> </div>
            <p>Static Events are the events when the car does not move.
        These include the cost report, the technical inspection, and the design aspects
        of competition. The first day of competition is dedicated to technical inspection,
        where judges scrutinize every inch of the car to make sure that it meets all of
        the competition rules and specifications. The second day is dedicated to the design
        aspect of competition, where teams are given the chance to explain to judges what
        separates their car from the rest, what testing was done to drive decisions, and
        to point out any other innovations. The cost report is submitted 8 weeks prior
        to competition and is also considered a static event. <br> <br> </p>

<!--      <div class = "row2"> -->
        <div class ="center">
           <h2>Dynamic Events</h2> </div>
           <p>Dynamic events take place on the third and fourth days of competition. The third day
            is exclusively for time trials. Each team races their car through a number of events
            including a maneuverability course, an acceleration track, a hill climb or tractor
            pull event, and a special event specified for each competition (water maneuverability,
            suspension and traction, or rock crawl). The fourth day of competition is reserved for
            a four hour endurance race in which all participating teams are on the same track at
            once, racing wheel-to-wheel. This part of the competition is the worth the
            most points and makes designing and fabrication for durability paramount to success.<br> <br> </p>

