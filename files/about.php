       <!--  <br> --> <h2>Competition Overview</h2> <!-- </div> --></div>
            <p>Cornell Baja SAE Racing is an engineering project team which annually designs,
        builds and races an off-road vehicle to compete in the SAE Collegiate Baja Design Series.
        The competition requires students to balance design and cost with dynamic performance
        while following a strict set of safety guidelines and standardized rules. There are
        three North American competitions annually: The East competition features an amphibious
        requirement of all vehicles and includes water maneuverability. The West competition
        includes a sales presentation and a rock crawl dynamic event. The Midwest competition
        has suspension and traction as the marquee event and also sometimes includes a mudbog.
        Each competition is four days long, with the first two days dedicated to static events
        and the last two days for dynamic events. <br> <br> </p>
